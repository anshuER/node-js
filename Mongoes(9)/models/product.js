const mongoose = require('mongoose');

//Point3: made the schema for product using mongoose.
const Schema = mongoose.Schema;

//Point4: create the schema for product.
const productSchema = new Schema({
  title: {
    type: String,
    require: true,
  },
  price: {
    type: Number,
    require: true,
  },
  description: {
    type: String,
    require: true,
  },
  imageUrl: {
    type: String,
    require: true,
  },
  userId: {
    type: Schema.Types.ObjectId,
    ref: 'User',
    require: true,
  },
});

//Point5: mongoose give you model function which create and export the function. it take 1st param and lower case and its plural and it to store as collection name
module.exports = mongoose.model('Product', productSchema);

